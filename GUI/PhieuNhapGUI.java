    private void mochitiet(PhieuNhapDTO pn,boolean xem){
        CTPhieuNhapGUI ctpm = null;
        try {
            if(pn!=null){
                ctpm = new CTPhieuNhapGUI(pn,xem);
                ctpm.setPn(pn);
                ctpm.update();
            }else ctpm = new CTPhieuNhapGUI();
            
        } catch (Exception ex) {
            Logger.getLogger(PhieuMuonGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        // Hiển thị JFrame chi tiết phiếu
        ctpm.setVisible(true);
        ctpm.setDefaultCloseOperation(ctpm.HIDE_ON_CLOSE);
    }
    private void mochitiet(){
        mochitiet(null,true);
    }
