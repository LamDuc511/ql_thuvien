    private void tablePhieuNhapMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablePhieuNhapMouseClicked
        // TODO add your handling code here:
        int rowIndex = tablePhieuNhap.getSelectedRow();
        if(rowIndex>=0){
            
            //Xuất ra txtNCC
            String value = tablePhieuNhap.getValueAt(rowIndex, 2).toString();
            if (value != null) {
                for (int i = 0; i < txtNCC.getItemCount(); i++) {
                String item = (String) txtNCC.getItemAt(i);
                    if (value.equals(item)) {
                        txtNCC.setSelectedItem(item);
                        break;
                    }
                }
            }
            //Xuất ra txtNV
            value = tablePhieuNhap.getValueAt(rowIndex, 1).toString();
            if (value != null) {
                for (int i = 0; i < txtNV.getItemCount(); i++) {
                String item = (String) txtNV.getItemAt(i);
                    if (value.equals(item)) {
                        txtNV.setSelectedItem(item);
                        break;
                    }
                }
            }
            // Xuất ra txtNgayNhap
            // Lấy giá trị của cột 2 trong dòng được chọn
            String dateString = tablePhieuNhap.getModel().getValueAt(rowIndex, 3).toString();

            // Chuyển đổi giá trị của cột 2 sang kiểu Date
            try {
                Date date = new SimpleDateFormat("yyyy-MM-dd").parse(dateString);
                // Đưa giá trị Date vào JDateChooser
                txtNgayNhap.setDate(date);
            } catch (ParseException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_tablePhieuNhapMouseClicked

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        //gồm 2 phần: thêm phiếu mượn mới vào db và mở chi tiết phiếu để nhập 
        PhieuNhapDTO sv =new PhieuNhapDTO();
        
        String[] parts;String part1,part2;
        parts = txtNCC.getSelectedItem().toString().trim().split(" ");
        part1 = parts[0];
        
        parts = txtNV.getSelectedItem().toString().trim().split(" ");
        part2 = parts[0];
        
        Date ngayNhap= txtNgayNhap.getDate();
        
        DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        // Chuyển đổi sang kiểu Date
        String strDateNhap = dateFormat.format(ngayNhap);
        
        sv.setMa(bus.getDefaultMaPN());
        sv.setMaNhaCungCap(Integer.parseInt(part1));
        sv.setMaNhanVien(Integer.parseInt(part2));
        sv.setNgayNhap(strDateNhap);

        try {
            if(bus.them(sv)){
                System.out.println("Them thanh cong");
                update();}
        } catch (Exception ex) {
            Logger.getLogger(PhieuNhapGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
//      Mở chi tiết phiếu vừa tạo
        mochitiet(sv,false);
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int rowIndex = tablePhieuNhap.getSelectedRow();
        if(rowIndex>=0){
            PhieuNhapDTO sv = new PhieuNhapDTO();
            
            String[] parts;String part1,part2;
            parts = txtNCC.getSelectedItem().toString().trim().split(" ");
            part1 = parts[0];

            parts = txtNV.getSelectedItem().toString().trim().split(" ");
            part2 = parts[0];

            Date ngayNhap = txtNgayNhap.getDate();
            DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            // Chuyển đổi sang kiểu Date
            String strDateNhap = dateFormat.format(ngayNhap);

            String value = tablePhieuNhap.getValueAt(rowIndex, 0).toString();

            
            sv.setMa(Integer.parseInt((String) value));
            sv.setMaNhaCungCap(Integer.parseInt(part1));
            sv.setMaNhanVien(Integer.parseInt(part2));
            sv.setNgayNhap(strDateNhap);
            sv.setTongTien(0);
            try {
                if(bus.sua(sv)){
                    System.out.println("Sua Thanh cong");
                    update();
                }
            } catch (Exception ex) {
                Logger.getLogger(PhieuNhapGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else JOptionPane.showMessageDialog(null, "Lỗi: Bạn Chưa chọn phiểu để Lưu", "Lỗi", JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_btnSuaActionPerformed

