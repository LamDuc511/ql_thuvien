    public void update(){
        try {
            // TODO add your handling code here:
            bus.docDB();
            Vector header =new Vector();
            header.add("Mã");
            header.add("Mã nhân viên");
            header.add("Mã đọc giả");
            header.add("Ngày mượn");
            header.add("Ngày trả");
            header.add("Ngày trả thực");
            header.add("Trạng thái");
            model = new DefaultTableModel(header,0);
            
            //thử hashmap
            HashMap<Integer, String> mapNhanVien = new HashMap<>();
            if(busNhanVien.getList_PM()==null){
                    busNhanVien.docDB();
            }
            for (NhanVienDTO sv : busNhanVien.getList_PM()){
                mapNhanVien.put(sv.getMa(), sv.getHo()+' '+sv.getTen());            
            }
            //
            
            for (PhieuMuonDTO sv : bus.getList_PM()){
                Vector row =new Vector();
                row.add(sv.getMa());
                
                // Lặp qua tất cả các key trong Map và lấy giá trị tương ứng
                for (int key : mapNhanVien.keySet()) {
                    String value = mapNhanVien.get(key);
                    if(sv.getMaNV()==key)
                        row.add(sv.getMaNV()+" "+value);
                }
                
                
                row.add(sv.getMaDG());
                row.add(sv.getNgayMuon());
                row.add(sv.getNgayTra());
                row.add(sv.getNgayTraThuc());
                row.add(sv.getTrangThai());
                model.addRow(row);
                
            }
            tablePhieuMuon.setModel(model);
        } catch (Exception ex) {
            Logger.getLogger(PhieuMuonGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
